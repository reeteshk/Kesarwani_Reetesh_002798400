    private void populateTable() {
        
        DefaultTableModel model=(DefaultTableModel)tblPersons.getModel();
    model.setRowCount(0);
    for(person p:history.getHistory())
    {
        Object [] row=new Object[4];
        row[0]=p.getName();
        row[1]=p.getAge();
        row[2]=p.getPhoneNumber();
        row[3]=p.getGender();
        model.addRow(row);
        
        
    }
    }
    private void populateTable(ArrayList<person> person) {
        
        DefaultTableModel model=(DefaultTableModel)tblPersons.getModel();
    model.setRowCount(0);
    for(person p:person)
    {
        Object [] row=new Object[4];
        row[0]=p.getName();
        row[1]=p.getAge();
        row[2]=p.getPhoneNumber();
        row[3]=p.getGender();
        model.addRow(row);
        
        
    }
    }
