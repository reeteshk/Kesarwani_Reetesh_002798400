    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
         int selectedRowIndex = tblPersons.getSelectedRow();
       
       if(selectedRowIndex<0)
       {
           JOptionPane.showMessageDialog(this, "Please select row to View,");
           return;
       }
       DefaultTableModel model=(DefaultTableModel)tblPersons.getModel();
       person selectedPerson=history.getPerson(model.getValueAt(selectedRowIndex, 0).toString());
        
      txtName.setText(selectedPerson.getName());
      txtAge.setText(String.valueOf(selectedPerson.getAge()));
      txtGender.setText(selectedPerson.getGender());
      txtPhoneNumber.setText(""+selectedPerson.getPhoneNumber());
       populateTable();
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        
         int selectedRowIndex = tblPersons.getSelectedRow();
       
       
       DefaultTableModel model=(DefaultTableModel)tblPersons.getModel();
       int count = model.getColumnCount();
       Object[] per=new Object[4];
       for(int i=0;i<count;i++){
           per[i] = model.getValueAt(selectedRowIndex,i);
       }
       history.deletePerson(model.getValueAt(selectedRowIndex, 0).toString());
       
     per[0] =txtName.getText();
     per[1]=Integer.parseInt(txtAge.getText());
     per[2]=Long.parseLong(txtPhoneNumber.getText());
     per[3]=txtGender.getText();
     history.addNewPerson(per[0].toString(),Integer.parseInt(per[1].toString()),Long.parseLong(per[2].toString()),per[3].toString());
     JOptionPane.showMessageDialog(this, "persons updated sucessfully");
    }//GEN-LAST:event_btnupdateActionPerformed


